#ifndef _9CC_
#define _9CC_

/* ----- Enum Definition----- */
// トークンの種類
typedef enum {
  TK_RESERVED, // 記号
  TK_NUM,      // 整数トークン
  TK_EOF,      // 入力の終わりを表すトークン
} TokenKind;

/* ----- Struct Definition----- */
typedef struct Token Token;
// トークン型
struct Token {
  TokenKind kind; // トークンの型
  Token *next;    // 次の入力トークン
  int val;        // kindがTK_NUMの場合、その数値
  char *str;      // トークン文字列
};

/* ----- Gloval Variable ----- */
extern Token *token;

#endif